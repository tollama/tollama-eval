"""Interactive Streamlit dashboard for tollama-eval.

Launch with:
    streamlit run -m ts_autopilot.reporting.dashboard
    streamlit run -m ts_autopilot.reporting.dashboard -- --artifact-dir out/
    streamlit run -m ts_autopilot.reporting.dashboard -- --results out/results.json

Or from code:
    python -m ts_autopilot.reporting.dashboard
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any


def _check_streamlit() -> bool:
    try:
        import streamlit  # noqa: F401

        return True
    except ImportError:
        return False


def main() -> None:
    """Entry point for the Streamlit dashboard."""
    if not _check_streamlit():
        print(
            "Streamlit is required for the dashboard. "
            'Install with: pip install "tollama-eval[dashboard]"',
            file=sys.stderr,
        )
        sys.exit(1)

    import streamlit as st

    st.set_page_config(
        page_title="tollama-eval Dashboard",
        page_icon="📈",
        layout="wide",
    )

    st.title("tollama-eval Interactive Dashboard")
    st.markdown("Run a benchmark from CSV or inspect saved benchmark artifacts.")
    cli_results_path, cli_details_path = _parse_dashboard_args(sys.argv[1:])

    if cli_results_path is not None:
        with st.sidebar:
            st.header("Loaded Artifacts")
            st.caption(str(cli_results_path))
            if cli_details_path is not None:
                st.caption(str(cli_details_path))
        _open_saved_results(cli_results_path, cli_details_path)
        return

    # Sidebar configuration
    with st.sidebar:
        st.header("Configuration")
        input_mode = st.radio(
            "Source",
            ["Run Benchmark", "Open Saved Results"],
            index=0,
        )

        uploaded_file = None
        results_file = None
        details_file = None
        horizon = 14
        n_folds = 3
        include_optional = False
        include_neural = False
        selected_models: list[str] = []
        run_button = False
        open_button = False

        if input_mode == "Run Benchmark":
            uploaded_file = st.file_uploader(
                "Upload CSV",
                type=["csv"],
                help="Long or wide format time series CSV",
            )
            horizon = st.number_input("Forecast Horizon", min_value=1, value=14)
            n_folds = st.number_input("CV Folds", min_value=1, value=3)
            include_optional = st.checkbox(
                "Include optional models",
                value=False,
                help="Safely discover optional non-core models in this environment.",
            )
            include_neural = st.checkbox(
                "Include neural models",
                value=False,
                disabled=not include_optional,
                help="Opt in to safe neural optional model discovery.",
            )

            from ts_autopilot.pipeline import DEFAULT_RUNNERS, resolve_default_runners

            if include_optional or include_neural:
                available_models = [
                    runner.name
                    for runner in resolve_default_runners(
                        include_optional=True,
                        include_neural=include_neural,
                    )
                ]
            else:
                available_models = [runner.name for runner in DEFAULT_RUNNERS]
            selected_models = st.multiselect(
                "Models",
                available_models,
                default=["SeasonalNaive", "AutoETS"],
            )

            run_button = st.button(
                "Run Benchmark", type="primary", use_container_width=True
            )
        else:
            results_file = st.file_uploader(
                "Upload results.json",
                type=["json"],
                help="Benchmark summary artifact generated by tollama-eval.",
            )
            details_file = st.file_uploader(
                "Upload details.json (optional)",
                type=["json"],
                help=(
                    "Optional details artifact containing forecasts, diagnostics, "
                    "and benchmark environment context."
                ),
            )
            open_button = st.button(
                "Open Results",
                type="primary",
                use_container_width=True,
            )

    if input_mode == "Open Saved Results":
        if results_file is None:
            st.info("Upload `results.json` to inspect a completed benchmark run.")
            return

        if not open_button:
            st.info("Click `Open Results` to load the uploaded artifacts.")
            return

        _open_saved_results(results_file, details_file)
        return

    if uploaded_file is None:
        st.info("Upload a CSV file to get started.")
        _show_sample_format()
        return

    if not run_button:
        # Show data preview
        import pandas as pd

        df = pd.read_csv(uploaded_file)
        st.subheader("Data Preview")
        st.dataframe(df.head(50), use_container_width=True)
        st.caption(f"{len(df):,} rows, {len(df.columns)} columns")
        return

    # Run benchmark
    _run_benchmark(
        uploaded_file,
        int(horizon),
        int(n_folds),
        selected_models,
        include_optional=include_optional,
        include_neural=include_neural,
    )


def _read_uploaded_json(uploaded_file: Any) -> dict[str, Any]:
    """Read JSON from an uploaded file or filesystem path."""
    if hasattr(uploaded_file, "getvalue"):
        raw = uploaded_file.getvalue()
        text = raw.decode("utf-8") if isinstance(raw, bytes) else str(raw)
    else:
        text = Path(uploaded_file).read_text(encoding="utf-8")
    return json.loads(text)


def _load_result_artifacts(
    results_file: Any,
    details_file: Any | None = None,
) -> Any:
    """Load BenchmarkResult from uploaded results.json and optional details.json."""
    from ts_autopilot.contracts import BenchmarkResult

    combined = _read_uploaded_json(results_file)
    if details_file is not None:
        combined.update(_read_uploaded_json(details_file))
    return BenchmarkResult.from_dict(combined)


def _parse_dashboard_args(argv: list[str]) -> tuple[Path | None, Path | None]:
    """Parse optional artifact path arguments passed through Streamlit."""
    parser = argparse.ArgumentParser(add_help=False)
    parser.add_argument("--artifact-dir")
    parser.add_argument("--results")
    parser.add_argument("--details")
    args, _ = parser.parse_known_args(argv)

    results_path = Path(args.results) if args.results else None
    details_path = Path(args.details) if args.details else None

    if args.artifact_dir:
        artifact_dir = Path(args.artifact_dir)
        if results_path is None:
            results_path = artifact_dir / "results.json"
        if details_path is None:
            candidate = artifact_dir / "details.json"
            if candidate.exists():
                details_path = candidate

    return results_path, details_path


def _open_saved_results(results_file: Any, details_file: Any | None = None) -> None:
    """Open and render saved benchmark artifacts in the dashboard."""
    import streamlit as st

    try:
        result = _load_result_artifacts(results_file, details_file)
    except Exception as exc:
        st.error(f"Failed to load benchmark artifacts: {exc}")
        return

    _render_result_dashboard(
        st,
        result,
        artifact_manifest=_artifact_manifest_summary(
            result,
            results_source=_artifact_source_label(results_file),
            details_source=_artifact_source_label(details_file),
            details_requested=details_file is not None,
        ),
    )


def _show_sample_format() -> None:
    """Show expected CSV format."""
    import streamlit as st

    st.subheader("Expected CSV Format")
    col1, col2 = st.columns(2)
    with col1:
        st.markdown("**Long format** (recommended)")
        st.code(
            "unique_id,ds,y\n"
            "series_1,2020-01-01,10.5\n"
            "series_1,2020-01-02,11.2\n"
            "series_2,2020-01-01,20.0",
            language="csv",
        )
    with col2:
        st.markdown("**Wide format**")
        st.code(
            "date,series_1,series_2\n2020-01-01,10.5,20.0\n2020-01-02,11.2,21.5",
            language="csv",
        )


def _run_benchmark(
    uploaded_file: Any,
    horizon: int,
    n_folds: int,
    model_names: list[str],
    *,
    include_optional: bool = False,
    include_neural: bool = False,
) -> None:
    """Execute benchmark and display results."""
    import tempfile

    import streamlit as st

    from ts_autopilot.ingestion.loader import load_csv
    from ts_autopilot.pipeline import (
        attach_optional_runner_report_context,
        resolve_default_runners,
        run_benchmark,
    )

    # Save uploaded file temporarily
    with tempfile.NamedTemporaryFile(suffix=".csv", delete=False) as tmp:
        tmp.write(uploaded_file.getvalue())
        tmp_path = tmp.name

    progress_bar = st.progress(0, text="Loading data...")

    try:
        df = load_csv(tmp_path)
        progress_bar.progress(10, text="Data loaded. Running benchmark...")

        # Progress tracking

        def progress_cb(step: str, current: int, total: int) -> None:
            if step == "model":
                pct = min(10 + int(80 * current / total), 90)
                progress_bar.progress(pct, text=f"Running model {current}/{total}...")

        runners = None
        if include_optional or include_neural:
            runners = list(
                resolve_default_runners(
                    include_optional=True,
                    include_neural=include_neural,
                )
            )

        result = run_benchmark(
            df,
            horizon=horizon,
            n_folds=n_folds,
            runners=runners,
            model_names=model_names if model_names else None,
            progress_callback=progress_cb,
        )
        attach_optional_runner_report_context(
            result,
            include_optional=include_optional,
            include_neural=include_neural,
        )
        progress_bar.progress(100, text="Benchmark complete!")

    except Exception as exc:
        st.error(f"Benchmark failed: {exc}")
        return
    finally:
        Path(tmp_path).unlink(missing_ok=True)

    _render_result_dashboard(st, result)


def _artifact_source_label(source: Any | None) -> str | None:
    """Return a human-readable label for an uploaded artifact source."""
    if source is None:
        return None
    if hasattr(source, "name") and source.name:
        return str(source.name)
    return str(source)


def _artifact_manifest_summary(
    result: Any,
    *,
    results_source: str,
    details_source: str | None,
    details_requested: bool,
) -> dict[str, Any]:
    """Summarize which saved artifacts were loaded and what they provide."""
    details = result.to_details_dict()
    details_loaded = bool(details)
    detail_features = []
    if result.forecast_data:
        detail_features.append("forecast data")
    if result.diagnostics:
        detail_features.append("diagnostics")
    if getattr(result, "data_characteristics", None) is not None:
        detail_features.append("data characteristics")
    if "optional_model_environment" in details:
        detail_features.append("environment provenance")

    detail_contents = ", ".join(detail_features) if detail_features else "no extras"
    rows = [
        {
            "Artifact": "results.json",
            "Status": "Loaded",
            "Source": results_source,
            "Contents": "profile, config, models, leaderboard",
        },
        {
            "Artifact": "details.json",
            "Status": "Loaded" if details_requested and details_loaded else "Missing",
            "Source": details_source or "not provided",
            "Contents": (
                detail_contents if details_requested and details_loaded else "-"
            ),
        },
    ]
    return {
        "rows": rows,
        "loaded_count": sum(1 for row in rows if row["Status"] == "Loaded"),
        "missing_count": sum(1 for row in rows if row["Status"] != "Loaded"),
    }


def _render_artifact_manifest(st: Any, manifest: dict[str, Any]) -> None:
    """Render a compact manifest for loaded benchmark artifacts."""
    if not manifest.get("rows"):
        return

    st.subheader("Artifact Manifest")
    st.caption(
        "Saved-result inputs loaded by the dashboard. Missing companions limit "
        "available views such as forecasts, diagnostics, and environment context."
    )
    col1, col2 = st.columns(2)
    col1.metric("Artifacts Loaded", manifest["loaded_count"])
    col2.metric("Artifacts Missing", manifest["missing_count"])
    st.dataframe(manifest["rows"], use_container_width=True, hide_index=True)


def _render_result_dashboard(
    st: Any,
    result: Any,
    *,
    artifact_manifest: dict[str, Any] | None = None,
) -> None:
    """Render a benchmark result in the dashboard."""
    import pandas as pd
    import plotly.graph_objects as go

    from ts_autopilot.reporting.executive_summary import generate_executive_summary

    st.divider()

    summary = generate_executive_summary(result)
    st.info(summary)

    if artifact_manifest:
        _render_artifact_manifest(st, artifact_manifest)

    st.subheader("Dataset Profile")
    col1, col2, col3, col4 = st.columns(4)
    col1.metric("Series", result.profile.n_series)
    col2.metric("Total Rows", f"{result.profile.total_rows:,}")
    col3.metric("Frequency", result.profile.frequency)
    col4.metric("Season Length", result.profile.season_length_guess)

    if result.warnings:
        with st.expander(f"Warnings ({len(result.warnings)})", expanded=False):
            for w in result.warnings:
                st.warning(w)

    _render_optional_model_environment(st, result)

    st.subheader("Leaderboard")
    lb_data = []
    for entry in result.leaderboard:
        lb_data.append(
            {
                "Rank": entry.rank,
                "Model": entry.name,
                "MASE": round(entry.mean_mase, 4),
                "SMAPE": f"{entry.mean_smape:.2f}%",
                "RMSSE": round(entry.mean_rmsse, 4),
                "MAE": round(entry.mean_mae, 4),
            }
        )
    if lb_data:
        st.dataframe(pd.DataFrame(lb_data), use_container_width=True, hide_index=True)

    if result.leaderboard:
        st.subheader("Model Comparison")

        names = [e.name for e in result.leaderboard]
        mase_vals = [e.mean_mase for e in result.leaderboard]
        bar_colors = ["#2563eb" if v < 1.0 else "#dc2626" for v in mase_vals]

        fig = go.Figure(
            go.Bar(
                y=names[::-1],
                x=mase_vals[::-1],
                orientation="h",
                marker_color=bar_colors[::-1],
                text=[f"{v:.4f}" for v in mase_vals[::-1]],
                textposition="outside",
            )
        )
        fig.add_vline(x=1.0, line_dash="dash", line_color="#9ca3af")
        fig.update_layout(
            title="MASE Comparison",
            xaxis_title="MASE (lower is better)",
            height=max(250, len(names) * 45 + 100),
        )
        st.plotly_chart(fig, use_container_width=True)

        if result.models and len(result.models[0].folds) > 1:
            fig2 = go.Figure()
            colors = [
                "#2563eb",
                "#dc2626",
                "#059669",
                "#d97706",
                "#7c3aed",
                "#db2777",
                "#0891b2",
                "#65a30d",
            ]
            for i, model in enumerate(result.models):
                fig2.add_trace(
                    go.Scatter(
                        x=[f"Fold {f.fold}" for f in model.folds],
                        y=[f.mase for f in model.folds],
                        name=model.name,
                        mode="lines+markers",
                        line={"color": colors[i % len(colors)]},
                    )
                )
            fig2.add_hline(y=1.0, line_dash="dash", line_color="#9ca3af")
            fig2.update_layout(title="MASE Stability Across Folds", height=350)
            st.plotly_chart(fig2, use_container_width=True)

    if result.models:
        st.subheader("Model Details")
        for model in result.models:
            with st.expander(f"{model.name} (MASE={model.mean_mase:.4f})"):
                fold_data = []
                for f in model.folds:
                    fold_data.append(
                        {
                            "Fold": f.fold,
                            "Cutoff": f.cutoff,
                            "MASE": round(f.mase, 4),
                            "SMAPE": f"{f.smape:.2f}%",
                            "RMSSE": round(f.rmsse, 4),
                            "MAE": round(f.mae, 4),
                        }
                    )
                if fold_data:
                    st.dataframe(
                        pd.DataFrame(fold_data),
                        use_container_width=True,
                        hide_index=True,
                    )
                st.caption(f"Runtime: {model.runtime_sec:.2f}s")


def _optional_model_environment_summary(result: Any) -> dict[str, Any]:
    """Return normalized optional model provenance from the result details view."""
    rows = result.to_details_dict().get("optional_model_environment", [])
    if not rows:
        return {"rows": []}

    enabled_groups = sum(1 for row in rows if row.get("available"))
    skipped_groups = len(rows) - enabled_groups
    enabled_models = sum(
        len(row.get("runner_names", [])) for row in rows if row.get("available")
    )
    table_rows = [
        {
            "Group": row.get("label", ""),
            "Status": "Enabled" if row.get("available") else "Skipped",
            "Models": ", ".join(row.get("runner_names", [])),
            "Detail": (
                "Dependency stack available"
                if row.get("available")
                else row.get("reason", "")
            ),
        }
        for row in rows
    ]
    return {
        "rows": table_rows,
        "enabled_groups": enabled_groups,
        "skipped_groups": skipped_groups,
        "enabled_models": enabled_models,
    }


def _render_optional_model_environment(st: Any, result: Any) -> None:
    """Render a compact benchmark environment block in the dashboard."""
    summary = _optional_model_environment_summary(result)
    if not summary["rows"]:
        return

    st.subheader("Benchmark Environment")
    st.caption(
        "Optional dependency discovery for this run. Skipped groups were not "
        "benchmarked in the current environment."
    )

    col1, col2, col3 = st.columns(3)
    col1.metric("Optional Models Enabled", summary["enabled_models"])
    col2.metric("Enabled Groups", summary["enabled_groups"])
    col3.metric("Skipped Groups", summary["skipped_groups"])
    st.dataframe(summary["rows"], use_container_width=True, hide_index=True)


if __name__ == "__main__":
    main()
